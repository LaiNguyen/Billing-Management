    private void prepareJTables() {
        prapareData();
        prepareTable();
        this.getContentPane().setBackground(BACKGROUND_COLOR);
    }
    
    private void prapareData() {
        this.allTables = Tables.getAll();
    }
    
    private void prepareTable() {
        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("Tên bàn");
        tableHeaders.add("Ngày tạo");
        tableHeaders.add("Người tạo");
        for (Object o : this.allTables) {
            Tables table  = (Tables) o;
            Vector<Object> oneRow = new Vector<Object>();
            oneRow.add(table.getTabName());
            oneRow.add(customFormat.format(table.getCreatedAt()));
            oneRow.add(table.getCreatedBy());
            tableData.add(oneRow);
        }
        resultTable.setModel(new DefaultTableModel(tableData, tableHeaders) {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        });
    }
    
