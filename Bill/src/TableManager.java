    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // insert into DB
        Tables table = new Tables();
        table.setTabName(txtSelectedName.getText());
        table.insert();
        prepareTable();
        
        // update UI
        DefaultTableModel tableModel = (DefaultTableModel) resultTable.getModel();
        Vector<Object> oneRow = new Vector<Object>();
        oneRow.add(table.getTabName());
        oneRow.add(customFormat.format(table.getCreatedAt()));
        oneRow.add(table.getCreatedBy());
        tableModel.addRow(oneRow);
        resultTable.setModel(tableModel);
        
        // update memory data
        allTables.add(table);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // update into DB
        int selectedRow = resultTable.getSelectedRow();
        Tables table = this.allTables.get(selectedRow);
        table.setTabName(txtSelectedName.getText());
        table.setUpdatedAt(new Date());
        table.setUpdatedBy("Lai");
        table.update();
        
        // update UI
        resultTable.getModel().setValueAt(table.getTabName(), selectedRow, 0);
        resultTable.getModel().setValueAt(table.getUpdatedBy(), selectedRow, 2);
    }//GEN-LAST:event_btnEditActionPerformed

