    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int[] selectedRows = resultTable.getSelectedRows();
        int[] removedIds = new int[selectedRows.length];
        // remove from DB
        for (int i = 0; i < selectedRows.length; i++) {
            int selectedIndex = selectedRows[i];
            Tables table = this.allTables.get(selectedIndex);
            table.delete();
            removedIds[i] = table.getId();
        }
        
        // update memory data
        for (Iterator<Tables> iterator = this.allTables.iterator(); iterator.hasNext();) {
            Tables table = iterator.next();
            if (IntStream.of(removedIds).anyMatch(i -> i == table.getId())) {
                // Remove the current element from the iterator and the list.
                iterator.remove();
            }
        }
        
        // updare UI
        prepareTable();
    }//GEN-LAST:event_btnRemoveActionPerformed

